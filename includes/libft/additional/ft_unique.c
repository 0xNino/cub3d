#include "../include/libft.h"

int	ft_unique(char *str, char c)
{
	int	i;

	i = 0;
	if (!str)
		return (1);
	while (str[i])
	{
		if (str[i] == c)
			i++;
		else
			return (1);
	}
	return (0);
}
